				</li>
				<li>
					<p>5.群资料页-群小程序—</p>
					<span>从微信iOS版迎来v6.5.13更新开始，新增群资料页查看群里所有收到过的小程序功能，群资料页可以查看最近收到的小程序，这也是不错的推广方式。</span>
				</li>
				<li>
					<p>6.附近的小程序—</p>
					<span>只要将您的小程序设置对应的位置，附近5KM范围内的用户即可搜索到您的小程序，并快速体验。这个入口为实体店服务做了很大的引流作用。</span>
				</li>
				<li>
					<p>7.小程序列表（历史记录）—</p>
					<span>只要您曾经使用过某个小程序，即可在小程序面板快速找到。并且它会按使用的先后顺序排序，便于您快速找到最近使用的服务。</span>
				</li>
				<li>
					<p>8.公众号菜单直达—</p>
					<span>商户可以在公众号菜单设置小程序入口，引导用户直接进入小程序，体验相关的服务。</span>
				</li>
				<li>
					<p>9.聊天窗口—</p>
					<span>—在于好友聊天界面中，点击“+”，可看到有“图片”“小视频”“位置”等选项，未来或许会增加“小程序的选项；</span>
				</li>
				<li>
					<p>10.公众号关联推送—</p>
